import * as esbuild from "esbuild";
import { mkdirSync, rmSync, writeFileSync } from "fs";
import { resolve } from "path";
import { randomUUID } from "crypto";
import { generateRuntime } from "./runtime-template.js";
import type { HandlerEntry } from "./extract-handlers.js";

export interface BundleOptions {
  configPath: string;
  handlers: HandlerEntry[];
  hooksDir: string;
}

export interface BundledFile {
  fileName: string;
  filePath: string;
  event: string;
  name: string;
  matcher: string | undefined;
  timeout: number | undefined;
  if: string | undefined;
  shell: "bash" | "powershell" | undefined;
  statusMessage: string | undefined;
  once: boolean | undefined;
  async: boolean | undefined;
  asyncRewake: boolean | undefined;
}

function toCamelCase(event: string): string {
  return event[0].toLowerCase() + event.slice(1);
}

export async function bundleHandlers(
  options: BundleOptions,
): Promise<BundledFile[]> {
  const { configPath, handlers, hooksDir } = options;
  const absConfigPath = resolve(configPath);

  mkdirSync(hooksDir, { recursive: true });

  writeFileSync(resolve(hooksDir, "runtime.cjs"), generateRuntime());

  const tmpDir = resolve(hooksDir, `.tmp-${randomUUID().slice(0, 8)}`);
  mkdirSync(tmpDir, { recursive: true });

  const results: BundledFile[] = [];

  try {
    for (const handler of handlers) {
      const name = handler.name;
      const event = handler.event;
      const fileName = `${toCamelCase(event)}-${name}.cjs`;
      const outPath = resolve(hooksDir, fileName);

      const entryContent = [
        `import { ${name} } from ${JSON.stringify(absConfigPath)};`,
        `const { run } = require(__dirname + "/runtime.cjs");`,
        `run(${name}.handler);`,
      ].join("\n");

      const entryPath = resolve(tmpDir, `${name}.ts`);
      writeFileSync(entryPath, entryContent);

      await esbuild.build({
        entryPoints: [entryPath],
        bundle: true,
        format: "cjs",
        platform: "node",
        outfile: outPath,
        external: [],
        banner: {
          js: "// Generated by clauptain-hook — do not edit",
        },
      });

      results.push({
        fileName,
        filePath: outPath,
        event,
        name,
        matcher: handler.matcher,
        timeout: handler.timeout,
        if: handler.if,
        shell: handler.shell,
        statusMessage: handler.statusMessage,
        once: handler.once,
        async: handler.async,
        asyncRewake: handler.asyncRewake,
      });
    }
  } finally {
    rmSync(tmpDir, { recursive: true, force: true });
  }

  return results;
}
